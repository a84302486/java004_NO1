package _14_Member;

public class ResultBean {


	private String result;
	
	public ResultBean() {
		
	}

	public ResultBean(String result) {
		super();
		this.result = result;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}
	
}
